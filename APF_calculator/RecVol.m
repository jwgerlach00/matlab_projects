function recVol = RecVol(recLength, recWidth, recHeight)

    % Calculates the volume of a rectangular prism
    % Inputs:    recLength -- length of side 1 (m)
    %            recWidth -- length of side 2 (m)
    %            recHeight -- length of side 3 (m)
    % Output:    recVol -- volume (m^3)
    
    recVol = recLength*recWidth*recHeight;
end
